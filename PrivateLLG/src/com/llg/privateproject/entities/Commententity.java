package com.llg.privateproject.entities;

import java.io.Serializable;
import java.util.List;
import java.util.Map;

/***
 * 评论实体类 yh 2015.6.23
 * 
 * */
public class Commententity implements Serializable {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	/** 用昵称 */
	public String petName;
	/** 用户头像 */
	public String imgHead;
	/** 评论内容 */
	public String content;
	/** 用户等级 */
	public String grade;
	/** 评论图片 */
	public int imgs[];
	/** 评论时间 */
	public String commentTime;
	/** 尺寸 */
	public String size;
	/** 颜色 */
	public String color;
	/** 分数 */
	public String score;
	/** 评论状态 */
	public String status;
	public List<String> listimgs;

	/**
	 * 评论实体类构造函数 昵称、头像、内容、等级、评论图片数组、评论时间、尺寸、颜色、分数、评论状态,评论图片数组
	 * */
	public Commententity(String petName, String imgHead, String content,
			String grade, int[] imgs, String commentTime, String size,
			String color, String score, String status, List<String> listimgs) {
		super();
		this.petName = petName;
		this.imgHead = imgHead;
		this.content = content;
		this.grade = grade;
		this.imgs = imgs;
		this.commentTime = commentTime;
		this.size = size;
		this.color = color;
		this.score = score;
		this.status = status;
		this.listimgs = listimgs;
	}

}
