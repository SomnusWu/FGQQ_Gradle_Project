package com.smartandroid.sa.verifi;

public class VerifiorException extends java.lang.Exception{
    private static final long serialVersionUID = 1L;

    public VerifiorException() {
        super();
    }

    /**
     * @param detailMessage
     * @param throwable
     */
    public VerifiorException(String detailMessage, Throwable throwable) {
        super(detailMessage, throwable);
    }

    /**
     * @param detailMessage
     */
    public VerifiorException(String detailMessage) {
        super(detailMessage);
    }

    /**
     * @param throwable
     */
    public VerifiorException(Throwable throwable) {
        super(throwable);
    }
}
